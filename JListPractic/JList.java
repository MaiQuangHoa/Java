    private void btnSwitch1to2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSwitch1to2ActionPerformed
        int stt = lstMH1.getSelectedIndex();
        if(stt != -1)
        {
        ListModel model = lstMH1.getModel();
        DSMHYeuThich.add(model.getElementAt(stt).toString());
        if(!DSMHTheThao.isEmpty())
        {
                DSMHTheThao.remove(stt); 
        }
        String[] up = DSMHTheThao.toArray(new String[0]);
        lstMH1.setListData(up);
        String[] addMH = DSMHYeuThich.toArray(new String[0]);
        lstMH2.setListData(addMH);
        }
        else
            JOptionPane.showMessageDialog( null,
                                     "Bạn chưa chọn giá trị"
                                             ,
                                     "Thông báo",
                                     JOptionPane.QUESTION_MESSAGE);
    }//GEN-LAST:event_btnSwitch1to2ActionPerformed

