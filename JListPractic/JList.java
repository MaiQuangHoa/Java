    private void Update()
    {
        ListModel model = lstMH1.getModel();
        for (int i = 0; i < model.getSize(); i++) {
            DSMHTheThao.add(model.getElementAt(i).toString());
        }
        model = lstMH2.getModel();
        for (int i = 0; i < model.getSize(); i++) {
            DSMHYeuThich.add(model.getElementAt(i).toString());
        }
    }
